public class City {

  private int cityNo;
  private String name;

  public City() {
  }

  public int getCityNo() {
    return cityNo;
  }

  public void setCityNo(int cityNo) {
    this.cityNo = cityNo;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
